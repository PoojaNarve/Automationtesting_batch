package looping;

public class forloop_example2 
{
	//program to print number from 50 to 200 => 50 51 ...200
	//starting point 50
	//end point 		 200
	//operator increment
	public static void main(String[] args) 
	{
		//50         201<=200  201
		for(int i=50;i<=200;++i)
		{
			System.out.print(i+" ");
		}
		
		
	}
}
