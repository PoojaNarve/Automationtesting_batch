package looping;

public class forloop_Example1 
{
//program to print number 1 to 5  output=> 1 2 3 4 5
//starting point intialiazation 1
// end point condition 5 true 
// increment 
	
	public static void main(String[] args) 
	{
		// 1	6<=5  6
		for(int b=1;b<=10;b++)
		{
		for(int a=1;a<=10;a++)
		{
			System.out.println(b*a);//1 2 3 4 5
		}
		System.out.print("");
		}
	}
}
