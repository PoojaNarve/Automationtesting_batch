package looping;

public class whileloop_example1 
{
	//program to print numbers from 1 to 100
	public static void main(String[] args) 
	{
		int a=1;//intialization
		while(a<=100)//condition 101<=100
		{
			System.out.println(a);//code for execution  1 2 3   ....  100
			a++;//increment operator   101
		}	
	}
}
