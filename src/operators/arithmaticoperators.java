package operators;

public class arithmaticoperators 
{
	public static void main(String[] args) 
	{
		int num1=30;//first operand
		int num2=20;//second operand
		int num3;
		
		//addition operator
		num3=num1+num2;
		System.out.println(num1+num2);//30+20 Addition operation
		
		//Substraction operator
		
		System.out.println(num1-num2);//30-20 Substraction
		
		//Multiplication
		
		System.out.println(num1*num2);//30*20
		
		//Division
		System.out.println(num1/num2);//30/20
		
		//Modulous
		System.out.println(num1%num2);//30%20
	}
}
